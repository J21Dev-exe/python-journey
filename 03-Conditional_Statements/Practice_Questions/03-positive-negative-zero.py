''' 
Check whether a number is:
Positive
Negative
Zero
'''

#===================================
# Solution:
print('3.')

# Print Positive, Negative, or Zero
number = int(input('Enter Number: '))

if (number > 0):                        # if condition - Positive
    print('Number is Positive')
elif (number < 0):                      # if condition - Negative
    print('Number is Negative!')
else:                                   # else condition - Zero
    print('Number is Zero')

