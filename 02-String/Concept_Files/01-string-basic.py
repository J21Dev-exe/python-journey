# String Basic Print

name1 = 'Hello'
name2 = "J21"

print("Hello J21")              # Print statement

print("Hello " + name2)         # Concatenation with double quote name2
print('Hello ' + name2)         # Concatenation with single quote name2

print(name1 +  " J21")          # Concatenation with double quote name1
print(name1 +  ' J21')          # Concatenation with single quote name1

print(name1 + ' ' + name2)      # Single quote with Spacebar
print(name1 + " " + name2)      # Double quote with Tab

