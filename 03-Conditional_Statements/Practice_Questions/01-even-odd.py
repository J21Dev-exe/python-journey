''' 
Take a number from the user and determine whether it is Even or Odd.
'''

#===================================
# Solution:
print('1.')

# Print Even or Odd
input = 10

if (input % 2 == 0):                        # if condition - even
    print(f'{input} is an Even Number')
else:                                       # else condition - odd
    print(f'{input} is an Odd Number')

