'''
Create a calculator capable of performing addition, subtraction, multiplication, and division operations
on two numbers. Your Program should format the output in a readable manner!
'''

#=========================================================================================================

# Solution: 

a = int(input('Enter first integer number: '))
b = int(input('Enter second integer number: '))

# addition
print(f'The addition result of {a} & {b} is: {a + b}')

# subtraction
print(f'The subtraction result of {a} & {b} is: {a - b}')

# multiplication
print(f'The multiplication result of {a} & {b} is: {a * b}')

# division
print(f'The division result of {a} & {b} is: {a / b}')