''' 
Take two numbers and print the larger number.
'''

#===================================
# Solution:
print('4.')

# Print Largest of two numbers
number1 = int(input('Enter number1: '))
number2 = int(input('Enter number2: '))

if (number1 > number2):                     # if condition - Num1 largest
    print(f'{number1}(number1) is largest')
