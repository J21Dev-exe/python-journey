# String methods - Checks if, printable, has space, is title, & covert to title

anime = 'Demon Slayer'
space = '   '
about = 'Anime Movie'

print(anime.isprintable())                  # Printable?
print(space.isspace())                      # Has Space?
print(about.istitle())                      # Is Title?
print('i am jeevan jyoti das'.title())      # Conversion - Title