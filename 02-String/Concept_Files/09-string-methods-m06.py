# String methods - Check if, alpha-numeric, only alphabets, Uppercase, Lowercase

username = 'J21'
name = 'Jeevan'
clg = 'BJB'
quote = 'hi'

print(username.isalnum())           # Alpha-numeric
print(name.isalpha())               # Alphabets
print(clg.isupper())                # Uppercase
print(quote.islower())              # Lowercase